/*
    _Notas
    _ Abstração
      - Permite isolar de um objeto somente os conceitos que são necessários
      - para o funcionamento do programa.
    _ Encapsulamento
      - Visa ocultar partes internas de um objeto e exibir apenas o necessário
      - para uso externo.
      # É basicamente ocultar aquilo que você não quer que fique esposto.
    _ Herança
      - Visa passar a característica de um objeto para outro.
      # Faz com que os objetos filhos tenham características herdadas dos pais.
    _ Polimorfismo
      - Algo que é polimorfo tem a habilidade de assumir diferentes formas.
      # Permite mudar as características de algo herdado do pai, fazendo assumir
      # mais de um comportamento
*/
